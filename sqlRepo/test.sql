-- 나이가 20살 이거나 30살 이거나 40살 회원 의 모든 칼럼 조회
SELECT * FROM MEMBER WHERE AGE = 20 OR AGE = 30 OR AGE = 40; 
SELECT * FROM MEMBER WHERE AGE IN (20,30,40);

