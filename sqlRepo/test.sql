-- LOCATION이 서울 인 회원 조회
SELECT * FROM MEMBER WHERE LOCATION = '서울';

-- LOCATION이 NULL 인 회원 조회
SELECT * FROM MEMBER WHERE LOCATION IS NULL;

-- 나이가 10 이상 25 이하
SELECT * FROM MEMBER WHERE AGE >= 10 AND AGE <= 25;
SELECT * FROM MEMBER WHERE AGE BETWEEN 10 AND 25;

-- 거주지가 울 이라는 글자로 끝나는 회원의 닉네임을 조회
SELECT NICK , LOCATION FROM MEMBER WHERE LOCATION LIKE '%울';

-- 거주지에 주 라는 글자가 들어가는 회원의 모든칼럼 조회
